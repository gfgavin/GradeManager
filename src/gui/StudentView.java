    public void setStudent(String username)
    {
        studentController = new StudentController(username);
        ArrayList<Course> courseList = studentController.getCourseList();
        DefaultTableModel model = (DefaultTableModel) studentClassTable.getModel();
        model.getDataVector().removeAllElements();
        int i = 0;
        for(Course t : courseList) {
            model.insertRow(i,new Object[]{t.toString()});
            i++;
            
        }
        for (int j = courseList.size(); j < 25 ; j++) {
            model.addRow(new Object[]{" "});
        }
    }

