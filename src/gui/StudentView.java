    private void viewGradesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewGradesActionPerformed
        int row = studentClassTable.getSelectedRow();

        ArrayList<Course> courseList = studentController.getCourseList();
        if (row > courseList.size() - 1) {
            return;
        }

        //show assignments and grades for selected course
        int courseId = courseList.get(row).getCourseID();
        String grades = studentController.viewGradesForCourse(courseId);

        gradesTextArea.setText(grades);
        gradesTextArea.update(gradesTextArea.getGraphics());
        
        //refresh the panel
        //ActionPerformed(evt);
    }//GEN-LAST:event_viewGradesActionPerformed

